        unsigned GaussLegendre1D::n_points() const
        {
            return _npts;
        }

        double GaussLegendre1D::weight(unsigned i) const
        {
            if (i >= _npts)
            {
                throw "Trying to access quadrature point that is greater than number computed";
            }
            return _weights[i];
        }

        double GaussLegendre1D::point(unsigned i) const
        {
            if (i >= _npts)
            {
                throw "Trying to access quadrature point that is greater than number computed";
            }
            return _points[i];
        }

