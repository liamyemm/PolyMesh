    unsigned GaussJacobi1D::n_points() const
    {
        return _npts;
    }

    double GaussJacobi1D::weight(unsigned i) const
    {
        if (i >= _npts)
        {
            throw "Trying to access quadrature point that is greater than number computed";
        }
        return _weights[i];
    }

    double GaussJacobi1D::point(unsigned i) const
    {
        if (i >= _npts)
        {
            throw "Trying to access quadrature point that is greater than number computed";
        }
        return _points[i];
    }

